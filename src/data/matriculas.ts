// Função para obter estatísticas dos dados
export function getMatriculasStats(data: Matricula[]) {
  const totalMatriculas = data.filter(m => m.Atividade === 'Matricula').length;
  const totalCancelamentos = data.filter(m => m.Atividade === 'Cancelamento').length;
  
  const plataformas = [...new Set(data.map(m => m.Plataforma))];
  const gestores = [...new Set(data.map(m => m.Gestor).filter(g => g))];
  const colaboradores = [...new Set(data.map(m => m.Colaborador).filter(c => c))];
  
  const cursosMaisPopulares = data.reduce((acc, curr) => {
    acc[curr.Curso] = (acc[curr.Curso] || 0) + 1;
    return acc;
  }, {} as Record<string, number>);

  // Estatísticas por colaborador
  const colaboradoresStats = colaboradores.map(colaborador => {
    const matriculasDoColaborador = data.filter(m => m.Colaborador === colaborador && m.Atividade === 'Matricula');
    return {
      nome: colaborador,
      totalMatriculas: matriculasDoColaborador.length,
      plataformasUsadas: [...new Set(matriculasDoColaborador.map(m => m.Plataforma))]
    };
  }).sort((a, b) => b.totalMatriculas - a.totalMatriculas);

  return {
    totalMatriculas,
    totalCancelamentos,
    totalPlataformas: plataformas.length,
    totalGestores: gestores.length,
    totalColaboradores: colaboradores.length,
    colaboradores: colaboradoresStats,
    cursosMaisPopulares: Object.entries(cursosMaisPopulares)
      .sort(([,a], [,b]) => b - a)
      .slice(0, 5)
  };
}